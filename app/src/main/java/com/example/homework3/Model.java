package com.example.homework3;

public class Model {
    private int photo;
    private String text;

    public Model(int photo, String text) {
        this.photo = photo;
        this.text = text;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;

    }
    }
